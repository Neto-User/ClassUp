    private void carregarAlunosNoPainel() throws SQLException {
        ColecaoAlunos colecao = new ColecaoAlunos();
        List<Aluno> lista = colecao.buscarTodosAlunos();
        
        PainelAlunos.removeAll();
        for (Aluno aluno : lista) {
            JLabel label = new JLabel(aluno.getNome());
            label.setFont(new Font("SansSerif", Font.PLAIN, 14));
            
            label.setBorder(BorderFactory.createEmptyBorder(1, 2, 1, 2));
            label.setPreferredSize(new Dimension(100, 25));
            label.setMaximumSize(new Dimension(Integer.MAX_VALUE, 25));
            
            PainelAlunos.add(label);
            
            // separa a tabela com as linhas 
            JSeparator separar = new JSeparator(SwingConstants.HORIZONTAL);
            separar.setPreferredSize(new Dimension(300, 2));
            separar.setMaximumSize(new Dimension(Integer.MAX_VALUE, 2));
            separar.setForeground(new Color(182, 170, 162));
            
            
            PainelAlunos.add(separar);
            
        }
        PainelAlunos.revalidate();
        PainelAlunos.repaint();
    }
    
